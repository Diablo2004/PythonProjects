# Converting types
num_str = "100"
num_int = int(num_str)  # Convert string to integer
print("Converted to Integer:", num_int)

num_float = float(num_int)  # Convert integer to float
print("Converted to Float:", num_float)

# Convert to string
print("Converted to String:", str(num_int))

# Boolean conversion
print("Boolean of 0:", bool(0))  # False
print("Boolean of non-zero:", bool(123))  # True

x = 5
y = 2.5
z = x + y
print(z)