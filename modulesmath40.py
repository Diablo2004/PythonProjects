import math
#module = a file containing code you want to include in your program using import


# from math import e
a=2
print(math.e**a)


# Square root
number = 16
sqrt_result = math.sqrt(number)
print(f"The square root of {number} is {sqrt_result}")

# Factorial
num = 5
factorial_result = math.factorial(num)
print(f"The factorial of {num} is {factorial_result}")

# Sine and Cosine
angle = math.radians(45)  # Convert degrees to radians
sin_value = math.sin(angle)
cos_value = math.cos(angle)

print(f"Sine of 45 degrees: {sin_value}")
print(f"Cosine of 45 degrees: {cos_value}")
